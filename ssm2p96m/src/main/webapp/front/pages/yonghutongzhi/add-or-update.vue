<script>
	import wPicker from "@/components/w-picker/w-picker.vue";

	export default {
		data() {
			return {
				ruleForm: {
												zhanghao: '',
																xingming: '',
																tongzhibiaoti: '',
																tongzhineirong: '',
																tongzhishijian: '',
												},
												zhanghaoOptions: [],
				zhanghaoIndex: 0,
																																												// 登陆用户信息
				user: {},
                                ro:{
                                   zhanghao : false,
                                   xingming : false,
                                   tongzhibiaoti : false,
                                   tongzhineirong : false,
                                   tongzhishijian : false,
                                },
			}
		},
		components: {
			wPicker
		},
		computed: {
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
					},
		async onLoad(options) {
        		this.ruleForm.tongzhishijian = this.$utils.getCurDateTime();

			let table = uni.getStorageSync("nowTable");
			// 获取用户信息
			let res = await this.$api.session(table);
			this.user = res.data;
			
						// ss读取
																																				
												// 下2
			res = await this.$api.option(`yonghu`,`zhanghao`,{});
			this.zhanghaoOptions = res.data;
																																	
			// 如果有登陆，获取登陆后保存的userid
			this.ruleForm.userid = uni.getStorageSync("userid")
			if (options.refid) {
				// 如果上一级页面传递了refid，获取改refid数据信息
				this.ruleForm.refid = options.refid;
				this.ruleForm.nickname = uni.getStorageSync("nickname");
			}
			// 如果是更新操作
			if (options.id) {
				this.ruleForm.id = options.id;
				// 获取信息
				res = await this.$api.info(`yonghutongzhi`, this.ruleForm.id);
				this.ruleForm = res.data;
			}
			// 跨表
			if(options.cross){
				var obj = uni.getStorageSync('crossObj');
				for (var o in obj){
					if(o=='zhanghao'){
					this.ruleForm.zhanghao = obj[o];
					this.ro.zhanghao = true;
					continue;
					}
					if(o=='xingming'){
					this.ruleForm.xingming = obj[o];
					this.ro.xingming = true;
					continue;
					}
					if(o=='tongzhibiaoti'){
					this.ruleForm.tongzhibiaoti = obj[o];
					this.ro.tongzhibiaoti = true;
					continue;
					}
					if(o=='tongzhineirong'){
					this.ruleForm.tongzhineirong = obj[o];
					this.ro.tongzhineirong = true;
					continue;
					}
					if(o=='tongzhishijian'){
					this.ruleForm.tongzhishijian = obj[o];
					this.ro.tongzhishijian = true;
					continue;
					}
				}
			}
			this.styleChange()
		},
		methods: {
			styleChange() {
				this.$nextTick(()=>{
					// document.querySelectorAll('.app-update-pv .cu-form-group .uni-input-input').forEach(el=>{
					//   el.style.backgroundColor = this.addUpdateForm.input.content.backgroundColor
					// })
				})
			},
												// 下二随
			async zhanghaoChange (e) {
				this.zhanghaoIndex = e.target.value
				this.ruleForm.zhanghao = this.zhanghaoOptions[this.zhanghaoIndex]
				let res = await this.$api.follow(`yonghu`, `zhanghao`,{
					columnValue: this.ruleForm.zhanghao
				});
																				if(res.data.xingming){
					this.ruleForm.xingming = res.data.xingming
				}
																																			},
																														
			// 多级联动参数
																																				
																																	
																																	// 日长控件选择日期时间
			tongzhishijianConfirm(val) {
				console.log(val)
				this.ruleForm.tongzhishijian = val.result;
				this.$forceUpdate();
			},
						
																																	
																																																
			getUUID () {
				return new Date().getTime();
			},
			async onSubmitTap() {
																																																																																																																																																																																																																																																																																																																																																																																																if(this.ruleForm.id){
					await this.$api.update(`yonghutongzhi`, this.ruleForm);
				}else{
					await this.$api.add(`yonghutongzhi`, this.ruleForm);
				}
				this.$utils.msgBack('提交成功');
			},
			optionsChange(e) {
				this.index = e.target.value
			},
			bindDateChange(e) {
				this.date = e.target.value
			},
			getDate(type) {
				const date = new Date();
				let year = date.getFullYear();
				let month = date.getMonth() + 1;
				let day = date.getDate();
				if (type === 'start') {
					year = year - 60;
				} else if (type === 'end') {
					year = year + 2;
				}
				month = month > 9 ? month : '0' + month;;
				day = day > 9 ? day : '0' + day;
				return `${year}-${month}-${day}`;
			},
			toggleTab(str) {
				this.$refs[str].show();
			}
		}
	}
</script>

